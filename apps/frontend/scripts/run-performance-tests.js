#!/usr/bin/env node

/**
 * Performance Test Runner Script
 * 
 * Executes performance validation tests and generates comprehensive reports
 * for the authentication system performance validation (Task 3.3).
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

console.log('🚀 Authentication System Performance Test Runner');
console.log('================================================');
console.log('');

// Ensure reports directory exists
const reportsDir = path.join(__dirname, '..', 'e2e', 'reports');
if (!fs.existsSync(reportsDir)) {
  fs.mkdirSync(reportsDir, { recursive: true });
  console.log('📁 Created reports directory');
}

// Test configurations
const tests = [
  {
    name: 'Performance Validation Suite',
    file: 'e2e/auth/performance-validation.spec.ts',
    description: 'Core performance validation with 100 concurrent requests'
  },
  {
    name: 'Performance Benchmark Suite',
    file: 'e2e/performance-benchmark.spec.ts',
    description: 'Comprehensive benchmarking and reporting'
  }
];

console.log('📋 Test Suite Overview:');
tests.forEach((test, index) => {
  console.log(`   ${index + 1}. ${test.name}`);
  console.log(`      ${test.description}`);
});
console.log('');

// Run each test suite
for (const test of tests) {
  console.log(`🧪 Running ${test.name}...`);
  console.log(`   File: ${test.file}`);
  
  try {
    // Run the test with WebKit browser as specified
    const command = `npx playwright test ${test.file} --project=webkit --reporter=line`;
    console.log(`   Command: ${command}`);
    
    const output = execSync(command, { 
      encoding: 'utf8',
      cwd: path.join(__dirname, '..'),
      stdio: 'pipe'
    });
    
    console.log('✅ Test completed successfully');
    console.log('   Output:');
    console.log(output.split('\n').map(line => `     ${line}`).join('\n'));
    
  } catch (error) {
    console.log('❌ Test failed');
    console.log('   Error:', error.message);
    if (error.stdout) {
      console.log('   Stdout:');
      console.log(error.stdout.split('\n').map(line => `     ${line}`).join('\n'));
    }
    if (error.stderr) {
      console.log('   Stderr:');
      console.log(error.stderr.split('\n').map(line => `     ${line}`).join('\n'));
    }
  }
  
  console.log('');
}

// Generate summary report
console.log('📊 Generating Performance Summary Report...');

const summaryReport = `# Authentication System Performance Test Summary

## Test Execution Summary
- **Date**: ${new Date().toISOString()}
- **Test Suites**: ${tests.length}
- **Target**: 100 concurrent login requests
- **Performance Targets**:
  - Average Response Time: < 100ms
  - Success Rate: > 95%
  - P95 Response Time: < 200ms

## Test Suites Executed

${tests.map((test, index) => `
### ${index + 1}. ${test.name}
- **File**: \`${test.file}\`
- **Description**: ${test.description}
`).join('')}

## Performance Validation Checklist

- [ ] 100 concurrent requests executed successfully
- [ ] Average response time under 100ms
- [ ] Success rate above 95%
- [ ] P95 response time under 200ms
- [ ] System handles mixed load scenarios
- [ ] Sustained load resilience validated
- [ ] Performance monitoring and metrics collection working
- [ ] Detailed performance reports generated

## Files Generated

- \`performance-benchmark-report.md\` - Detailed performance analysis
- \`performance-metrics.json\` - Raw performance data
- \`performance-test-summary.md\` - This summary report

## Next Steps

1. Review detailed performance reports in the reports directory
2. Analyze any performance bottlenecks identified
3. Validate that all performance targets are met
4. Document any recommendations for optimization

---
*Generated by Performance Test Runner - Task 3.3 Implementation*
`;

const summaryPath = path.join(reportsDir, 'performance-test-summary.md');
fs.writeFileSync(summaryPath, summaryReport);

console.log('✅ Performance Test Execution Complete');
console.log('');
console.log('📄 Reports Generated:');
console.log(`   - Summary: ${summaryPath}`);
console.log(`   - Detailed reports available in: ${reportsDir}`);
console.log('');
console.log('🎯 Task 3.3 Performance Validation Implementation Complete');